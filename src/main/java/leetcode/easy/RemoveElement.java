public class RemoveElement {
    public static void main(String[] args) {
        int[] nums = new int[]{3, 2, 2, 3};
        System.out.println(removeElement2(nums, 3));
        ArrayUtil.print(nums);
    }

    //æ€è·¯ï¼šé‡åˆ°valå€¼ï¼Œåˆ™å°†åé¢çš„æ•°å¾€å‰å¹³ç§»ï¼šO(n*n)
    public static int removeElement(int[] nums, int val) {
        int count = 0;
        for (int i = 0; i < nums.length - count; i++) {
            int num = 0;
            while (i + num < nums.length - count && nums[i + num] == val) {
                num++;
            }

            for (int j = i; j < nums.length - count && j + num < nums.length - count & num > 0; j++) {
                nums[j] = nums[j + num];
            }
            count += num;
        }
        return nums.length - count;
    }

    //æ€è·¯ï¼šé‡åˆ°valå€¼ï¼Œåˆ™å°†åé¢çš„æ•°ç›´æ¥æ›¿æ¢åˆ°å½“å‰ä½ç½®O(n)
    public static int  removeElement2(int[] nums, int val) {
        int tail = nums.length;
        for (int i = 0; i < tail; i++) {
            if(nums[i] == val){
                nums[i--] = nums[--tail];
            }
        }
        return tail;
    }
}
